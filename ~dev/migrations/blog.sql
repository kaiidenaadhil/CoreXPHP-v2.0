CREATE TABLE `blog` (
`blogId` INT  NOT NULL,
`title` VARCHAR(255)  NOT NULL,
`slug` VARCHAR(255)  NOT NULL,
`summary` VARCHAR(255)  NOT NULL,
`content` VARCHAR(255)  NOT NULL,
`category` VARCHAR(100)  NOT NULL,
`tags` VARCHAR(255)  NOT NULL,
`authorId` INT  NOT NULL,
`viewCount` INT  NOT NULL CHECK (`viewCount` <= 11),
`isFeatured` BOOLEAN  NOT NULL,
`isPublished` BOOLEAN  NOT NULL,
`publishedAt` TIMESTAMP  NULL,
`metaTitle` VARCHAR(255)  NOT NULL,
`metaDescription` VARCHAR(255)  NOT NULL,
`blogCreatedAt` TIMESTAMP  NOT NULL DEFAULT CURRENT_TIMESTAMP,
`blogUpdatedAt` TIMESTAMP  NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
`blogIdentify` VARCHAR(50)  NOT NULL,
FOREIGN KEY (`authorId`) REFERENCES `users`(`userId`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;